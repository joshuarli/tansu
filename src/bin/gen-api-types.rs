use std::{env, fs, path::PathBuf, process};

use ts_rs::TS;

use tansu::api_types::*;
use tansu::settings::Settings;

// Frontend API DTOs are owned by Rust and exported with ts-rs. When adding an
// endpoint shape, define the Rust type in src/api_types.rs (or the owning module
// for shared types like Settings), derive TS, add it below, and use
// #[ts(optional)] for fields that serde may omit.
fn decl<T: TS + 'static>() -> String {
    T::export_to_string(&ts_rs::Config::new().with_large_int("number"))
        .unwrap_or_else(|e| panic!("failed to export {}: {e}", std::any::type_name::<T>()))
}

fn split_fields(fields: &str) -> Vec<String> {
    let mut result = Vec::new();
    let mut start = 0usize;
    let mut depth = 0usize;

    for (idx, ch) in fields.char_indices() {
        match ch {
            '{' => depth += 1,
            '}' => depth = depth.saturating_sub(1),
            ',' if depth == 0 => {
                let field = fields[start..idx].trim();
                if !field.is_empty() {
                    result.push(field.to_string());
                }
                start = idx + ch.len_utf8();
            }
            _ => {}
        }
    }

    let field = fields[start..].trim();
    if !field.is_empty() {
        result.push(field.to_string());
    }

    result
}

fn format_decl(src: &str) -> String {
    let Some((prefix, rest)) = src.trim().split_once(" = {") else {
        return src.trim().to_string();
    };
    let Some(fields_src) = rest.strip_suffix("};") else {
        return src.trim().to_string();
    };

    let fields = split_fields(fields_src.trim().trim_end_matches(','));
    let single_line = format!("{} = {{ {} }};", prefix, fields.join("; "));
    if single_line.len() <= 100 {
        return single_line;
    }

    let mut out = format!("{prefix} = {{\n");
    for field in fields {
        out.push_str("  ");
        out.push_str(&field);
        out.push_str(";\n");
    }
    out.push_str("};");
    out
}

fn generate() -> String {
    let mut out = String::new();
    out.push_str(
        "// This file is generated by `cargo run --bin gen-api-types`. Do not edit manually.\n\n",
    );

    let types = [
        decl::<FieldScores>(),
        decl::<NoteResponse>(),
        decl::<SaveResult>(),
        decl::<SearchHit>(),
        decl::<NoteEntry>(),
        decl::<TagListResponse>(),
        decl::<FileSearchResult>(),
        decl::<RecentFileEntry>(),
        decl::<PinnedFileEntry>(),
        decl::<RenameResponse>(),
        decl::<FilenameResponse>(),
        decl::<ContentResponse>(),
        decl::<OkResponse>(),
        decl::<PutNoteRequest>(),
        decl::<CreateNoteRequest>(),
        decl::<RenameRequest>(),
        decl::<PinRequest>(),
        decl::<SessionState>(),
        decl::<AppStatus>(),
        decl::<VaultEntry>(),
        decl::<UnlockRequest>(),
        decl::<PrfRegisterRequest>(),
        decl::<PrfRemoveRequest>(),
        decl::<Settings>(),
    ];

    for ty in types {
        let body = ty
            .split_once("\n\n")
            .map(|(_, body)| body.trim())
            .unwrap_or_else(|| ty.trim());
        out.push_str(&format_decl(body));
        out.push_str("\n\n");
    }
    out.pop();

    out
}

fn main() {
    let path = PathBuf::from("web/ts/api.generated.ts");
    let out = generate();

    if env::args().any(|arg| arg == "--check") {
        let current = fs::read_to_string(&path)
            .unwrap_or_else(|e| panic!("failed to read {}: {e}", path.display()));
        if current != out {
            eprintln!(
                "{} is out of date. Run `cargo run --quiet --bin gen-api-types`.",
                path.display()
            );
            process::exit(1);
        }
        return;
    }

    fs::write(&path, out).unwrap_or_else(|e| panic!("failed to write {}: {e}", path.display()));
}
